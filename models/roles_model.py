class RolesCollection:
    def __init__(self, 
                 nombre_rol, 
                 descripcion, 
                 fecha_alta, 
                 fecha_modificacion, 
                 fecha_baja, 
                 estado):
        self.nombre_rol = nombre_rol
        self.descripcion = descripcion
        self.fecha_alta = fecha_alta
        self.fecha_modificacion = fecha_modificacion
        self.fecha_baja = fecha_baja
        self.estado = estado